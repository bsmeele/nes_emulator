  TXA
  TAY
  INY
  SEC
  BCS INSERTIONSORT.OUTER_LOOP
INSERTIONSORT.END_OUTER:
  RTS

// 0x0030-0x0031 is the pointer to the list
// The length of the list is written to 0x0032 by the caller
QUICKSORT.SETUP:
  LDX 0x32
  DEX
  LDY $0x00
QUICKSORT.START:
  CPY #0x00
  BCC QUICKSORT.END
  CPX #0x00
  BCC QUICKSORT.END
  STX tmp_hi
  CPY tmp_hi
  BCS QUICKSORT.END
  TXA
  PHA
  TYA
  PHA
  JSR PIVOT.START  // returne value is passed through accumulator
  TAX
  PLA
  TAY
  TXA
  PHA
  JSR QUICKSORT.START  // returne value is passed through accumulator
  PLA
  TAY
  INY
  PLA
  TAX
  JSR QUICKSORT.START
QUICKSORT.END:
  RTS

PIVOT.START:
  // y = lo, x = hi
  LDA (0x30),Y  // Acc = pivot
  DEY  // y = i
  INX  // x = j
PIVOT.LOOP:
PIVOT.I_LOOP:
  INY
  CMP (0x30),Y
  BCC PIVOT.I_LOOP_END
  BEQ PIVOT.I_LOOP_END
  BCS PIVOT.I_LOOP
PIVOT.I_LOOP_END:
  STX tmp_j
  STY tmp_i
  LDX tmp_i  // x = i
  LDY tmp_j  // y = j
PIVOT.J_LOOP:
  DEY
  CMP (0x30),Y
  BCC PIVOT.J_LOOP
PIVOT.J_LOOP_END:
  STY tmp_j
  CPX tmp_j
  BCC PIVOT.SWAP
  TYA
  RTS  // Return with Acc - j
PIVOT.SWAP:
  STA tmp_pivot
  STX tmp_i
  LDA (0x30),Y  // Acc = A[j]
  STA tmp_A_j
  LDY tmp_i
  LDA (0x30),Y  // Acc = A[i]
  LDY tmp_j
  STA (0x30),y
  LDY tmp_i
  LDA tmp_A_j
  STA (0x30),Y
  LDA tmp_pivot
  LDX tmp_j
  CLC
  BCC PIVOT.LOOP
